/// Web服务核心模块
/// 
/// 该模块提供了Web服务所需的核心功能，包括：
/// - 用户认证和授权
/// - 错误处理和响应
/// - JWT令牌管理
/// - 分页和查询
/// - 操作日志记录
/// - 请求追踪
/// 
/// # 模块结构
/// 
/// ## auth 模块
/// 提供用户认证和授权功能：
/// - 用户登录和注销
/// - 权限验证
/// - 角色管理
/// - 会话管理
/// 
/// ## error 模块
/// 提供统一的错误处理机制：
/// - 错误类型定义
/// - 错误转换
/// - 错误响应格式化
/// - 错误日志记录
/// 
/// ## jwt 模块
/// 提供JWT令牌的处理功能：
/// - 令牌生成
/// - 令牌验证
/// - 令牌刷新
/// - 令牌黑名单
/// 
/// ## page 模块
/// 提供分页查询功能：
/// - 分页参数处理
/// - 排序参数处理
/// - 查询条件构建
/// - 分页结果格式化
/// 
/// ## res 模块
/// 提供统一的响应处理：
/// - 响应格式定义
/// - 成功响应处理
/// - 错误响应处理
/// - 响应状态码管理
/// 
/// ## util 模块
/// 提供通用工具函数：
/// - 字符串处理
/// - 时间处理
/// - 加密解密
/// - 其他辅助功能
/// 
/// ## validator 模块
/// 提供请求数据验证：
/// - 字段验证规则
/// - 自定义验证器
/// - 验证错误处理
/// - 验证结果转换
/// 
/// ## operation_log 模块
/// 提供操作日志功能：
/// - 日志记录
/// - 日志查询
/// - 日志分析
/// - 日志导出
/// 
/// ## request_id 模块
/// 提供请求追踪功能：
/// - 请求ID生成
/// - 请求ID传递
/// - 请求追踪
/// - 日志关联

/// 认证模块
/// 
/// 提供用户认证和授权功能
pub mod auth;

/// 错误处理模块
/// 
/// 提供统一的错误处理机制
pub mod error;

/// JWT令牌模块
/// 
/// 提供JWT令牌的处理功能
pub mod jwt;

/// 分页模块
/// 
/// 提供分页查询功能
pub mod page;

/// 响应处理模块
/// 
/// 提供统一的响应处理
pub mod res;

/// 工具函数模块
/// 
/// 提供通用工具函数
pub mod util;

/// 输入验证模块
/// 
/// 提供请求数据验证
pub mod validator;

/// 操作日志模块
/// 
/// 提供操作日志功能
pub mod operation_log;

/// 请求ID模块
/// 
/// 提供请求追踪功能
mod request_id;

/// 导出请求ID相关类型
/// 
/// - RequestId: 请求ID类型
/// - RequestIdLayer: 请求ID中间件层
pub use request_id::{RequestId, RequestIdLayer};
